import { writeFileSync } from 'node:fs';
import { resolve } from 'node:path';

import { createLogger } from '../_lib/logger.mjs';
import { root } from '../_lib/paths.mjs';
import { FONT_OVERLAY, SYSTEM_FONT_ENTRY } from './font-overlay.mjs';

const log = createLogger('gen:fonts');
const METADATA_URL = 'https://fonts.google.com/metadata/fonts';
const CODING_WEIGHTS = [300, 400, 500, 600, 700];

const outFile = resolve(root, 'apps/renderer/src/app/editor/font-catalogue.ts');

const response = await fetch(METADATA_URL);
if (!response.ok) {
  log.error(`Google Fonts metadata failed: ${response.status} ${response.statusText}`);
  process.exit(1);
}

const metadata = JSON.parse((await response.text()).replace(/^\)\]\}'\s*/, ''));
const byFamily = new Map(metadata.familyMetadataList.map((font) => [font.family, font]));

const missing = FONT_OVERLAY.filter((entry) => !byFamily.has(entry.family));
if (missing.length > 0) {
  log.error(
    `Curated fonts missing from Google Fonts metadata:\n${missing
      .map((entry) => `  - ${entry.family}`)
      .join('\n')}`,
  );
  process.exit(1);
}

const monospaceCount = metadata.familyMetadataList.filter((font) =>
  /monospace/i.test(font.category),
).length;

const catalogue = [
  SYSTEM_FONT_ENTRY,
  ...FONT_OVERLAY.map((entry) => {
    const weights = pickCodingWeights(uprightWeights(byFamily.get(entry.family)));
    return {
      family: entry.family,
      weights,
      ligatures: entry.ligatures,
      note: entry.note,
    };
  }),
];

writeFileSync(outFile, renderCatalogue(catalogue), 'utf8');
log.info(
  `Wrote ${catalogue.length} fonts to ${outFile} (${monospaceCount} monospace on Google Fonts).`,
);

function uprightWeights(font) {
  return Object.keys(font.fonts ?? {})
    .filter((key) => /^\d+$/.test(key))
    .map(Number)
    .sort((a, b) => a - b);
}

function pickCodingWeights(available) {
  const preferred = CODING_WEIGHTS.filter((weight) => available.includes(weight));
  return preferred.length > 0 ? preferred : available;
}

function renderCatalogue(entries) {
  const rows = entries.map((entry) => {
    const family =
      entry.family === SYSTEM_FONT_ENTRY.family ? 'SYSTEM_FONT' : JSON.stringify(entry.family);
    return [
      '  {',
      `    family: ${family},`,
      `    weights: [${entry.weights.join(', ')}],`,
      `    ligatures: ${entry.ligatures},`,
      `    note: ${JSON.stringify(entry.note)},`,
      '  },',
    ].join('\n');
  });

  return `/**
 * GENERATED by \`pnpm gen:fonts\` — do not edit by hand.
 * Curated metadata lives in \`scripts/gen/font-overlay.mjs\`.
 */

export interface FontChoice {
  /** The family name, exactly as Google spells it. */
  family: string;
  /** The weights Google actually serves. Asking for one it does not have 400s. */
  weights: readonly number[];
  /** Whether the face carries programming ligatures (\`=>\`, \`!==\`, \`->\`). */
  ligatures: boolean;
  /** Shown under the name in the picker. */
  note: string;
}

/**
 * The one entry that is not a Google font: whatever the machine calls its
 * monospace. Always available, never fetched, and the honest choice for anyone
 * who has already installed the font they want to code in.
 */
export const SYSTEM_FONT = ${JSON.stringify(SYSTEM_FONT_ENTRY.family)};

export const FONT_CATALOGUE: readonly FontChoice[] = [
${rows.join('\n')}
];
`;
}
